package Switch;

public class test1 {
	public static void main(String[] args) {
		int input=2;
		
		switch (input) {
		case 1:
			System.out.println("Todays is monday");
			break;
			
		case 2:
			System.out.println("Todays is tuesday");
			break;
		case 3:
			System.out.println("Todays is wen");
			break;
		case 4:
			System.out.println("Todays is thus");
			break;
		case 5:
			System.out.println("Todays is friday");
			break;
		case 6:
			System.out.println("Todays is sat");
			break;
			
		case 7:
			System.out.println("Todays is Sun");
			break;
		default:
			System.out.println("invalid input");
			break;
		}
		
		
	}

}
